package internal

import (
	"time"

	"github.com/golang/protobuf/ptypes/timestamp"

	"github.com/open-telemetry/opentelemetry-collector/consumer/pdata"
)

// AddMetric adds an uninitialized metric to the metric slice and returns it.
func AddMetric(metrics pdata.MetricSlice) pdata.Metric {
	len := metrics.Len()
	metrics.Resize(len + 1)
	return metrics.At(len)
}

// TimeToTimestamp converts a time.Time to a timestamp.Timestamp pointer.
func TimeToTimestamp(t time.Time) *timestamp.Timestamp {
	if t.IsZero() {
		return nil
	}
	nanoTime := t.UnixNano()
	return &timestamp.Timestamp{
		Seconds: nanoTime / 1e9,
		Nanos:   int32(nanoTime % 1e9),
	}
}

// TimestampToTime converts a timestamp.Timestamp pointer to a time.Time.
func TimestampToTime(ts *timestamp.Timestamp) (t time.Time) {
	if ts == nil {
		return
	}
	return time.Unix(ts.Seconds, int64(ts.Nanos))
}
